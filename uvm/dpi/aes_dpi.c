static const unsigned char RCON[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

static unsigned char xt(unsigned char a) {
    return (a << 1) ^ ((a & 0x80) ? 0x1b : 0x00);
}

static void sub_bytes(unsigned char s[16]) {
    int i; for (i = 0; i < 16; i++) s[i] = SBOX[s[i]];
}

static void shift_rows(unsigned char s[16]) {
    unsigned char t[16];
    memcpy(t, s, 16);
    s[0]=t[0]; s[4]=t[4]; s[8]=t[8];  s[12]=t[12];
    s[1]=t[5]; s[5]=t[9]; s[9]=t[13]; s[13]=t[1];
    s[2]=t[10];s[6]=t[14];s[10]=t[2]; s[14]=t[6];
    s[3]=t[15];s[7]=t[3]; s[11]=t[7]; s[15]=t[11];
}

static void mix_columns(unsigned char s[16]) {
    int c;
    for (c = 0; c < 4; c++) {
        int i = c * 4;
        unsigned char r0=s[i], r1=s[i+1], r2=s[i+2], r3=s[i+3];
        s[i]   = xt(r0) ^ (xt(r1)^r1) ^ r2 ^ r3;
        s[i+1] = r0 ^ xt(r1) ^ (xt(r2)^r2) ^ r3;
        s[i+2] = r0 ^ r1 ^ xt(r2) ^ (xt(r3)^r3);
        s[i+3] = (xt(r0)^r0) ^ r1 ^ r2 ^ xt(r3);
    }
}

static void add_rk(unsigned char s[16], const unsigned char rk[16]) {
    int i; for (i = 0; i < 16; i++) s[i] ^= rk[i];
}

static void key_expand(const unsigned char key[16], unsigned char rks[11][16]) {
    int r, i;
    unsigned char tmp[4];
    memcpy(rks[0], key, 16);
    for (r = 1; r <= 10; r++) {
        tmp[0] = SBOX[rks[r-1][13]] ^ RCON[r];
        tmp[1] = SBOX[rks[r-1][14]];
        tmp[2] = SBOX[rks[r-1][15]];
        tmp[3] = SBOX[rks[r-1][12]];
        for (i = 0; i < 4; i++) rks[r][i] = rks[r-1][i] ^ tmp[i];
        for (i = 4; i < 16; i++) rks[r][i] = rks[r-1][i] ^ rks[r][i-4];
    }
}

static void aes128_enc(const unsigned char pt[16], const unsigned char key[16],
                       unsigned char ct[16]) {
    unsigned char rks[11][16], st[16];
    int r;
    key_expand(key, rks);
    memcpy(st, pt, 16);
    add_rk(st, rks[0]);
    for (r = 1; r <= 9; r++) {
        sub_bytes(st); shift_rows(st); mix_columns(st); add_rk(st, rks[r]);
    }
    sub_bytes(st); shift_rows(st); add_rk(st, rks[10]);
    memcpy(ct, st, 16);
}

//============================================================================
// DPI-C Export: called from SystemVerilog
//
// SV signature (packed 128-bit — no svOpenArrayHandle, no xsim runtime dep):
//   import "DPI-C" function void aes_encrypt_dpi(
//       input  bit [127:0] key_in,
//       input  bit [127:0] pt_in,
//       output bit [127:0] ct_out
//   );
//
// svBitVecVal layout for bit[127:0]:
//   index 0 = bits [31:0]  (LSW)
//   index 3 = bits [127:96] (MSW)
// AES byte 0 (MSB) lives in word 3 bits[31:24].
//============================================================================
void aes_encrypt_dpi(
    const svBitVecVal* key_in,   /* bit[127:0] — 4 x uint32, word0=LSW */
    const svBitVecVal* pt_in,
    svBitVecVal*       ct_out
) {
    unsigned char key[16], pt[16], ct[16];
    int i, w, sh;

    for (i = 0; i < 16; i++) {
        w  = 3 - i / 4;            /* word index: MSW first */
        sh = 24 - (i % 4) * 8;    /* shift: 24,16,8,0 within each word */
        key[i] = (key_in[w] >> sh) & 0xFF;
        pt[i]  = (pt_in[w]  >> sh) & 0xFF;
    }

    aes128_enc(pt, key, ct);

    for (i = 0; i < 4; i++) ct_out[i] = 0;
    for (i = 0; i < 16; i++) {
        w  = 3 - i / 4;
        sh = 24 - (i % 4) * 8;
        ct_out[w] |= ((svBitVecVal)ct[i]) << sh;
    }
}
